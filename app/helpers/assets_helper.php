if (!function_exists('acentosHtml')) {
    function acentosHtml($cadena = null, $modo = true) {
        $str = str_replace('Á', '&oacute;', $cadena);
        $str = str_replace('É', '&eacute;', $cadena);
        $str = str_replace('Í', '&iacute;', $cadena);
        $str = str_replace('Ó', '&oacute;', $cadena);
        $str = str_replace('Ú', '&uacute;', $cadena);
        $str = str_replace('Ñ', '&ntilde;', $cadena);
       return ucwords(strtolower(str_replace('Ó', '&oacute;', $str)));
    }
}

